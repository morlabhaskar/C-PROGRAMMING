#include <stdio.h>

int main(){
    int first,second,ans;
    printf("Enter a first Number :");
    scanf("%d",&first);
    printf("Enter a second Number :");
    scanf("%d",&second);
    ans = first + second;
    printf("The Sum of given numbers is %d",ans);
    return 0;
}

// OP : Enter a first Number : 5
// Enter a second Number : 10
// The Sum of given numbers is 15